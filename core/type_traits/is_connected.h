#ifndef IS_CONNECTED_H
#define IS_CONNECTED_H
//------------------------------------------------------------------------------------------------
template <class pin> struct is_connected
{
    static constexpr bool value = true;
};
//------------------------------------------------------------------------------------------------
template <> struct is_connected <NC>
{
    static constexpr bool value = false;
};
//------------------------------------------------------------------------------------------------
template <class pin>
inline constexpr bool connected = is_connected <pin> :: value;
//------------------------------------------------------------------------------------------------
#endif
